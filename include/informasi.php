          <div class="card-body">
            <h5 class="card-title">Berapa ketinggian air yang diperlukan untuk tanaman?</h5>
            <p class="card-text" style="text-align: justify">
              &nbsp;&nbsp;&nbsp;&nbsp;Batas-batas ketinggian air yang tepat untuk sawah dapat berbeda-beda
              tergantung pada jenis tanaman yang dibudidayakan, kondisi iklim, dan jenis tanah di wilayah tersebut.
              Namun, secara umum terdapat batas-batas ketinggian air yang dapat dijadikan acuan untuk pengaturan irigasi sawah,
              antara lain sebagai berikut:
            </p>
            <ul style="text-align: justify">
              <li>
                Ketinggian air rendah (0-10 cm): Ketinggian air ini cocok untuk tanaman yang membutuhkan irigasi terus menerus, seperti padi, jagung, dan tanaman sayuran. Namun, perlu diingat bahwa ketinggian air terlalu rendah dapat menyebabkan kekeringan pada tanaman dan mengurangi hasil panen.
              </li>
              <br>
              <li>
                Ketinggian air sedang (10-20 cm): Ketinggian air ini cocok untuk tanaman yang tidak memerlukan irigasi terus menerus, seperti sayuran yang lebih toleran terhadap kekeringan. Ketinggian air sedang juga dapat mengurangi risiko kelebihan air dan penyakit yang disebabkan oleh kelebihan air pada tanaman.
              </li>
              <br>
              <li>
                Ketinggian air tinggi (20-30 cm): Ketinggian air ini cocok untuk tanaman yang membutuhkan kelembaban yang tinggi, seperti tanaman padi yang masih berumur pendek. Namun, ketinggian air terlalu tinggi dapat menyebabkan kelebihan air pada tanaman dan menghambat pertumbuhan akar.
              </li>
            </ul>
