            <h5 class="card-title"><strong>Irigasi Otomatis</strong></h5>
            <h6 style="text-align:justify">
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Irigasi pada pertanian merupakan salah satu hal terpenting yang dapat
              menunjang keberhasilan panen dengan kualitas yang baik. Namun, rata-rata irigasi di daerah pertanian
              masih menggunakan cara yang tradisional, yaitu dengan cara datang langsung ke sawah untuk mendistribusikan
              air atau mematikan airnya. Oleh karena itu, untuk dapat mempermudah pekerjaan petani diciptakan
              sebuah alat yang dapat mengatur irigasi secara otomatis. Alat irigasi otomatis tersebut didasarkan pada
              pengukuran ketinggian air yang diharapkan dapat menjadi solusi yang efektif dan efisien untuk memastikan
              ketersediaan air yang tepat pada tanaman di sawah.
              <br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Sistem ini didesain untuk dapat mengatur pasokan air
              secara otomatis sesuai dengan ketinggian air yang terukur. Dalam sistem ini, terdapat sensor ketinggian air
              yang terpasang di area lahan sawah. Sensor ini akan secara otomatis mengukur ketinggian air dan memberikan
              informasi kepada sistem kontrol irigasi. Sistem kontrol irigasi kemudian akan memutuskan kapan harus memasok air ke sawah
              berdasarkan pada ketinggian air yang terukur.
            </h6>
